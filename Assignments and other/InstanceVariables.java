class InstanceVariables
{
	int instanceVar1 = 10; // Instance Variable 
	float instanceVar2 = 20; // Instance variable
	
	public static void main(String args[])
	{
		InstanceVariables iv = new InstanceVariables();
		
		System.out.println("Instance Variables are: instanceVar1 = "+iv.instanceVar1+" and instanceVar2 = "+iv.instanceVar2);
	}
}